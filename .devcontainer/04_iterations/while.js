// =========WHILE=============

let index = 0
while (index<=10) {
    console.log(`value of index is ${index}`);
    index+=2
}

let array = ["Flash","Power_ranger", "Dora"]
let arr = 0
while(arr<array.length){
    console.log(`value of my range ${array[arr]}`);
    arr+=1
}

//=============DO-WHILE================

let score =1
do {
    console.log(`score is ${score}`);
    score++
} while (score<=10);
